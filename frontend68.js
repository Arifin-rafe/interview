// image vs figure tag?
// The <img> tag is used to embed images in an HTML document,
// while the <figure> tag is a semantic container that groups media content (like images, illustrations, diagrams)
// along with their captions (using the <figcaption> tag) to provide context and meaning.

// meter tag vs progress tag?
// The <meter> tag represents a scalar measurement within a known range,
// such as disk usage or battery level, and is used for discrete values.
// The <progress> tag represents the completion progress of a task,
// typically displayed as a progress bar, and is used for continuous values.

// css border vs css outline?
// The border is part of the element's box model and affects layout;
// it can be styled on each side and supports border-radius for rounded corners.
// The outline is drawn outside the element, doesn't affect layout, and cannot be styled on each side or have rounded corners.
// Use borders for visual design and outlines mainly for accessibility or focus indication.
