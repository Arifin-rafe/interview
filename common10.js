// 1. What is JavaScript?

// JavaScript is a high-level, interpreted programming language used to make web pages interactive. It runs in the browser and on servers (Node.js).

// 2. Difference between var, let, and const?

// var → function-scoped, can be redeclared

// let → block-scoped, can be reassigned

// const → block-scoped, cannot be reassigned

// 3. What is hoisting?

// Hoisting is JavaScript’s behavior of moving variable and function declarations to the top of their scope before execution.

// 4. What is closure?

// A closure is when a function remembers and can access variables from its outer scope even after the outer function has finished executing.

// 5. What is this keyword?

// this refers to the object that is executing the current function, and its value depends on how the function is called.

// 6. Difference between == and ===?

// == compares values with type conversion
// === compares both value and type (strict equality)

// 7. What is an arrow function?

// Arrow functions are a shorter syntax for writing functions and they do not have their own this.

// 8. What is event bubbling?

// Event bubbling is when an event starts from the target element and propagates upward to its parent elements.

// 9. What is a promise?
// A promise is an object that represents the eventual completion or failure of an asynchronous operation.

// 10. Difference between null and undefined?
// undefined → variable declared but not assigned
// null → intentional absence of a value