// why we use useEffect in reactjs?
// useEffect is a lifecycle hook in React that allows you to perform side effects in your components.
// It is used to run code when the component mounts, updates, or unmounts a well as fetch data from an API.

// what is event binding?
// Event binding in React refers to the process of attaching event handlers to elements in your components.
// This allows you to respond to user interactions such as clicks, form submissions, and keyboard input.

// what is the purpose of random method in reactjs?
// The random method is often used in React to generate unique keys for lists of elements.
// This helps React efficiently update and render components by providing a stable identity for each element in a list.

// what is forwardref in reactjs?
// forwardRef is a React function that allows you to pass a ref through a component to one of its children.
// This is useful when you want to access a child component's DOM node or instance from a parent component.

// What is key in reactjs?
// In React, a key is a special string attribute you need to include when creating lists of elements.
// Keys help React identify which items have changed, are added, or are removed, improving performance during re-renders.

// useNavigate in reactjs?
// useNavigate is a hook provided by React Router that allows you to 
// programmatically navigate between routes in your application.
// It returns a function that you can call with a path to navigate to that route.

// difference between router and route in reactjs?
// In React Router, a Router is a component that manages the routing context for your application,
// while a Route is a component that defines a mapping between a URL path and a specific component to render.
// The Router provides the overall routing functionality, while Routes define individual routes within that context.

//section vs div tag in reactjs?
