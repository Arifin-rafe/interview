// what is async function?
// An async function is a function declared with the `async` keyword.
// It allows you to write asynchronous code that looks and behaves like synchronous code.
// It always returns a Promise. Inside an async function, you can use the `await` keyword
// to pause execution until a Promise is resolved.

// what is lazy loading?
// Lazy loading is a design pattern where components are loaded only when they are needed,
// rather than loading everything at the start. In React, this is achieved using `React.lazy()`
// and `Suspense`. It helps improve performance by reducing the initial load time (code splitting).