// What i a call stack?
//A call stack is a data structure used by programming languages like JavaScript to keep track of function calls. 
//It operates on a Last In, First Out (LIFO) principle, meaning the last function that was called is the first one to be completed and removed from the stack. 
//When a function is invoked, it is added to the top of the call stack, and when it finishes executing, it is removed from the stack. 
//This helps manage the execution context and ensures that functions are executed in the correct order.

